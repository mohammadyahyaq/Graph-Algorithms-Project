

package GraphPackage;

public class Set {

    private int v_one;
    private int v_two;
    private int weight;

    public Set(int v_one, int v_two, int weight){

        this.v_one = v_one;
        this.v_two = v_two;
        this.weight = weight;
    }

    public int getV_one() {
        return v_one;
    }

    public void setV_one(int v_one) {
        this.v_one = v_one;
    }

    public int getV_two() {
        return v_two;
    }

    public void setV_two(int v_two) {
        this.v_two = v_two;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    



}